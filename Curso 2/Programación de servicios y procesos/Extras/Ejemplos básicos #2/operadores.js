const a = 12
const b = 5

var c = a + b;
var d = a - b;
var e = a * b;
var f = a / b;
var g = a % b;
var h = a ** b;

console.log('a + b = ' + c)     // Suma
console.log('a - b = ' + d)     // Resta
console.log('a * b = ' + e)     // Multiplicación
console.log('a / b = ' + f)     // División
console.log('a % b = ' + g)     // Resto (división)
console.log('a ** b = ' + h)    // Potencia