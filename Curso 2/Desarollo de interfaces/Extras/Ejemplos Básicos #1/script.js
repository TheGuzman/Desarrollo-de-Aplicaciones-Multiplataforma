function sumar() {
    let operando1 = 23;
    let operando2 = 17;

    operando2 += operando1;

    document.writeln("Operación sumar: ", operando2);
}

function restar() {
    let operando1 = 11;
    let operando2 = 8;

    operando2 -= operando1;

    document.writeln("Operación restar: ", operando2);
}

sumar()
restar()