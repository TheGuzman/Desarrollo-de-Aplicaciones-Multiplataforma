# Ejemplo de booleanos

aT, aF = True, False #declaración dos vbles booleanas
print ("El valor es", aT, "de tipo:", type(aT), "\n") # \n equivale a salto línea
print ("El valor es", aF, "de tipo:", type(aF))

# Función bool para convertir a su valor booleano
print("El cero equivale a False y el 1 a True. Lo comprobamos:")
print(bool(0))
print(bool(1))

print("La cadena vacía es equivalente a False. Lo mismo sucederá con tuplas. listas, diccionarios y conjuntos")
print(bool(""))

print("\n Ejemplos de expresiones con tipos booleanos")
print(False == False)


